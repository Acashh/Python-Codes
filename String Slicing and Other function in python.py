#String slicing and other functions
my_str = "hello my name is Aakash"
print(my_str)
#string slicing
print(my_str[0:5])
#type of the my_str
print(type(my_str))
#length of the my_str
print(len(my_str))
print(my_str[0:40])
#extended slicing in python
print(my_str[0:])
print(my_str[:5])
print(my_str[:])
print(my_str[::2])
print(my_str[::])
print(my_str[0:23:1])
print(my_str[::2])
#negative and positive indexing in python
print(my_str[-6:])
print(my_str[17:])
print(my_str[::-1])
print(my_str[::-2])
#functions in python
print(my_str.isalnum())
print(my_str.isalpha())
print(my_str.endswith("Aakash"))
print(my_str.endswith("Acash"))
print(my_str.count("a"))
print(my_str.capitalize())
print(my_str.find("is"))
print(my_str.lower())
print(my_str.upper())
print(my_str.replace("my", "its"))


